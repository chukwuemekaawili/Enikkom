export const getAssetUrl = (filePath: string): string => {
  // If it's a system asset path, return the bundled URL
  if (filePath.startsWith('/src/assets/')) {
    return systemAssetMap[filePath] || filePath;
  }
  // Otherwise return as-is (for uploaded assets from storage)
  return filePath;
};
